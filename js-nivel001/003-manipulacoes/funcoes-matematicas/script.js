// arredondamento
let num = Math.round(3.67)
console.log(num)

num = Math.round(5.4)
console.log(num)

num = Math.floor(3.99) // arredonda para baixo
console.log(num)

num = Math.ceil(3.1) // arredonda para cima
console.log(num)

num = Math.abs(-9.63) // valor absoluto do número
console.log(num)

num = Math.min(8, 3, 2, 9, 4) // retorna o menor número
console.log(num)

num = Math.max(8, 9, 3, 26, 4, 7) // retorna o maior número
console.log(num)

num = Math.random() // retorna um número aleatório entre 0 e 1
console.log(num)